package day01_javaGiris;

public class C02_VariableKullanimi {

    public static void main(String[] args) {

        int sayi; // icine tamsayi koyacagim, sayi isminde bir variable olusturmak istiyorum

        // System.out.println(sayi); henuz bir deger atanmadigi icin kullanmayiz


        String str = "Java Candir";

        System.out.println(str); // Java Candir

        System.out.println("str"); // str


        sayi = 10;

        System.out.println(sayi); // 10

        sayi = 50;

        System.out.println(sayi); // 50
    }
}
